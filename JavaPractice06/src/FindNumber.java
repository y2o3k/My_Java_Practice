import java.util.Random;
import java.util.Scanner;

public class FindNumber {
	public static void main(String[] args) {
	//	숫자의 범위 0~99로 설정
Random rand = new Random();
Scanner stdIn = new Scanner(System.in);
	
final int MAX_NO = 6;//플레이어가 입력할수있는 횟수를 제한 
int leftNo = MAX_NO;
int no = rand.nextInt(100);		//if 제한 횟수 내에 맞추지 못한 경우 

System.out.println("숫자 맞추기 게임 시작!");		//정답을 표시 & 게임을 종료 
System.out.println("0부터 99사이의 숫자를 맞추세요.");
		
int x;  //플레이어가 입력한 값 
do {
	System.out.println("남은 횟수" + (leftNo--)+"회, 어떤 숫자일까?:");
	x = stdIn.nextInt();
	
	if(x>no)
		System.out.println("더 작은 숫자입니다.");
	else if (x<no)
		System.out.println("더 큰 숫자입니다.");
}while(x !=no && leftNo>0);

if(x==no)
	System.out.println((MAX_NO - leftNo)+ "회만에 맞췄습니다.");
else
	System.out.println("아쉽네요. 정답은" +no + "입니다.") ;
}
}
		

	
